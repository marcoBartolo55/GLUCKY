
package Clases;
public class dietas {
    
    private int id_dietas;
    private String titulo_dieta, descripcion_dieta;

    public int getId_dietas() {
        return id_dietas;
    }

    public void setId_dietas(int id_dietas) {
        this.id_dietas = id_dietas;
    }

    public String getTitulo_dieta() {
        return titulo_dieta;
    }

    public void setTitulo_dieta(String titulo_dieta) {
        this.titulo_dieta = titulo_dieta;
    }

    public String getDescripcion_dieta() {
        return descripcion_dieta;
    }

    public void setDescripcion_dieta(String descripcion_dieta) {
        this.descripcion_dieta = descripcion_dieta;
    }
    
    
    
}
