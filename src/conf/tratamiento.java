
package Clases;
public class tratamiento {
    
    private int id_tratamiento;
    private String titulo_tratamiento, descripcion_tratamiento;

    public int getId_tratamiento() {
        return id_tratamiento;
    }

    public void setId_tratamiento(int id_tratamiento) {
        this.id_tratamiento = id_tratamiento;
    }

    public String getTitulo_tratamiento() {
        return titulo_tratamiento;
    }

    public void setTitulo_tratamiento(String titulo_tratamiento) {
        this.titulo_tratamiento = titulo_tratamiento;
    }

    public String getDescripcion_tratamiento() {
        return descripcion_tratamiento;
    }

    public void setDescripcion_tratamiento(String descripcion_tratamiento) {
        this.descripcion_tratamiento = descripcion_tratamiento;
    }
    
        
}
